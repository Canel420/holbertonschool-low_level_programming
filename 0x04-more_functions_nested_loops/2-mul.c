/**
 * mul - Function that multiply integers.
 * @a: First integer
 * @b: Second integer
 * Description: Checks if a character input is a digit.This function multiply
 * two integers.
 * Return: integer multiplication.
 */

int mul(int a, int b)
{
	int mult;

	mult = (a * b);
	return (mult);
}
